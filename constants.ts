// --- LOCATION GENERATOR ---
const generateLocations = (): GeoLocation[] => {
    const locations: GeoLocation[] = [];
    const departments = new Map<string, string>(); // Name -> ID
    const provinces = new Map<string, string>(); // Name -> ID (Unique key DEP-PROV)

    const lines = RAW_GEO_DATA.split('\n');
    let depCount = 0;
    let provCount = 0;
    let distCount = 0;

    lines.forEach(line => {
        const parts = line.split('\t');
        if (parts.length < 4) return;
        const distName = parts[1].trim();
        const provName = parts[2].trim();
        const depName = parts[3].trim();

        // 1. Department
        let depId = departments.get(depName);
        if (!depId) {
            depCount++;
            depId = `DEP-${depCount}`;
            departments.set(depName, depId);
            locations.push({ id: depId, name: depName, type: 'DEP' });
        }

        // 2. Province
        const provKey = `${depId}-${provName}`;
        let provId = provinces.get(provKey);
        if (!provId) {
            provCount++;
            provId = `PROV-${provCount}`;
            provinces.set(provKey, provId);
            locations.push({ id: provId, name: provName, type: 'PROV', parentId: depId });
        }

        // 3. District
        distCount++;
        locations.push({ id: `DIST-${distCount}`, name: distName, type: 'DIST', parentId: provId });
    });

    return locations;
};

export const MOCK_LOCATIONS: GeoLocation[] = generateLocations();
